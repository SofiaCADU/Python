estrellas = int(input('¿Cuántas estrellas tiene el restaurante? (1-5): '))

if estrellas >= 4.5:
    print('Extraordinario')
elif estrellas >= 4:
    print('Excelente')
elif estrellas >= 3:
    print('Bueno')
elif estrellas >= 2:
    print('Regular')
else:
    print('Pesimo')




    